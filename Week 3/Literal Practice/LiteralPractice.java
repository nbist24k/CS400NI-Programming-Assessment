public class LiteralPractice{
    public static void main(String[] args){
        // Long literal
        long largeNum = 2555555555000L;
        
        // Float literal 
        float num = 2.55f;
        
        //Char literal using unicode escape sequence
        char symbol='\u0024';
        
        System.out.println("Long value: " + largeNum);
        System.out.println("Float value: " + num);
        System.out.println("Char value: " + symbol);
        
    }
}