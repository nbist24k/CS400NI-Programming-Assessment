public class DataTypeInspector{
    public static void main(String[] args){
        // Declaring and initializing for each primitive types
        byte byteValue = 100;
        short shortValue = 10000;
        int intValue = 100000;
        long longValue = 1000000L;
        float floatValue = 234.5f;
        double doubleValue = 123.4;
        char charValue = 'A';
        boolean booleanValue = true;
        
        
        System.out.println("======== Data Type Inspector ========");
        System.out.println("Byte Value: " + byteValue);
        System.out.println("Short Value: " + shortValue);
        System.out.println("Int Value: " + intValue);
        System.out.println("Long Value: " + longValue);
        System.out.println("Float Value: " + floatValue);
        System.out.println("Double Value: " + doubleValue);
        System.out.println("Char Value: " + charValue);
        System.out.println("Boolean Value: " + booleanValue);
        
        
    }
}